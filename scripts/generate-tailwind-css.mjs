import fs from "node:fs";
import path from "node:path";
import { compile } from "@tailwindcss/node";

const rootDir = process.cwd();
const sourcePath = path.join(rootDir, "src/app/globals.tailwind.css");
const outputPath = path.join(rootDir, "src/app/globals.css");
const candidatePattern = /[\w:/.[\]()%#,-]+/g;

function walk(dir, files = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (entry.name === "node_modules" || entry.name === ".next" || entry.name.startsWith(".")) {
      continue;
    }

    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      walk(fullPath, files);
    } else if (/\.(ts|tsx|css)$/.test(entry.name)) {
      files.push(fullPath);
    }
  }

  return files;
}

function collectCandidates() {
  const candidates = new Set();
  const sourceFiles = walk(path.join(rootDir, "src")).filter((file) => file !== outputPath);

  for (const file of sourceFiles) {
    const content = fs.readFileSync(file, "utf8");
    for (const match of content.matchAll(candidatePattern)) {
      const token = match[0];
      if (token.length > 1) {
        candidates.add(token);
      }
    }
  }

  return {
    files: sourceFiles.length,
    values: [...candidates],
  };
}

const sourceCss = fs.readFileSync(sourcePath, "utf8");
const compiler = await compile(sourceCss, {
  from: sourcePath,
  base: rootDir,
  onDependency() {},
});

const candidates = collectCandidates();
const outputCss = compiler.build(candidates.values);

fs.writeFileSync(
  outputPath,
  `/* Generated by scripts/generate-tailwind-css.mjs from src/app/globals.tailwind.css */\n${outputCss}\n`
);

console.log(
  `Generated ${path.relative(rootDir, outputPath)} from ${candidates.files} files with ${candidates.values.length} candidates (${outputCss.length} chars)`
);
